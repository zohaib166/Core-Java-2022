public class Operators {
    public static void main(String[] args) {
        int num1 = 45;
        int num2 = 23;
        System.out.println(num1%num2);
        System.out.println(num1==num2);
        System.out.println(num1+=1);
        System.out.println(num2++);
        System.out.println(num1>50 && ++num2<100);
        System.out.println(num2);

    }
   
}
